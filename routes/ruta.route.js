/** =====================================================================
 *  RUTAS ROUTER 
=========================================================================*/
const { Router } = require('express');
const { check } = require('express-validator');

// MIDDLEWARES
const { validarCampos } = require('../middlewares/validar-campos');
const { validarJWT } = require('../middlewares/validar-jwt');

// CONTROLLERS
const { getRutas, getRutaId, createRuta, updateRuta } = require('../controllers/rutas.controller');

const router = Router();

/** =====================================================================
 *  POST RUTAS
=========================================================================*/
router.post('/query', getRutas);
/** =====================================================================
 *  POST RUTAS
=========================================================================*/

/** =====================================================================
 *  GET RUTA ID
=========================================================================*/
router.get('/:id', validarJWT, getRutaId);
/** =====================================================================
 *  GET RUTA ID
=========================================================================*/

/** =====================================================================
 *  POST CREATE RUTA
=========================================================================*/
router.post('/', [
        validarJWT,
        check('name', 'El nombre es olbigatorio').not().isEmpty(),
        validarCampos
    ],
    createRuta
);
/** =====================================================================
 *  POST CREATE RUTA
=========================================================================*/

/** =====================================================================
 *  PUT RUTA
=========================================================================*/
router.put('/:id', validarJWT, updateRuta);
/** =====================================================================
 *  PUT RUTA
=========================================================================*/

// EXPORT
module.exports = router;